    private void jListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jListMouseClicked
        if (evt.getClickCount() == 2) {
            int selectedIndex = jList.getSelectedIndex();
            if (selectedIndex != -1) {
                Vuelo vuelo = sis.getListaVuelos().get(selectedIndex);
                sis.autorizarVuelo(vuelo);
                JOptionPane.showMessageDialog(null, "La salida del vuelo ha sido autorizada correctamente", "Vuelo Autorizado", JOptionPane.INFORMATION_MESSAGE);
                mostrarVuelos();
            }
        }
    }//GEN-LAST:event_jListMouseClicked

