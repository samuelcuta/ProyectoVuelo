    public void mostrarVuelos() {
        DefaultListModel<String> model = new DefaultListModel<>();
        LinkedList<Vuelo> vuelos = sis.getListaVuelos();
        for (Vuelo vuelo : vuelos) {
            model.addElement(vuelo.toString()); 
        }
        jList.setModel(model);
    }

    
