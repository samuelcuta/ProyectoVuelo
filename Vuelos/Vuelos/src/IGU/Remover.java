    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        numFl = jtxtFieldNumFl.getText();
        vuelo = sis.buscarVuelo(numFl);
        if(jtxtFieldNumFl.getText().trim().isEmpty())
            JOptionPane.showMessageDialog(null, "Porfavor rellene la casilla con la informacion solicitada para poder continuar", "Complete las casillas", JOptionPane.INFORMATION_MESSAGE);
        else{
            if(sis.buscarVuelo(numFl) != null){
                jtxtFieldInfo.setText(vuelo.toString());
            }
            else{
                jtxtFieldInfo.setText("Vuelo no Encontrado");
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

